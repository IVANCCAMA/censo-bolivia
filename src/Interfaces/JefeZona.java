    private String getZonaCensal(){
        zona = "";
        try {
            String sql = "select ZonaCensal from censo.usuario where  codUsr = " ;
            sent = conn.createStatement();
            ResultSet rs = sent.executeQuery(sql);
            rs.next(); 
            zona = rs.getString("ZonaCensal");

            } catch (Exception e) {
                e.printStackTrace();
            }
        return zona;
    }
    
